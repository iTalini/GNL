#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int			i;

	i = 0;
	while (i == 0 || (i > 0 && s[i - 1] != '\0'))
	{
		if (s[i] == (char)c)
			return ((char *)(&s[i]));
		i++;
	}
	return (0);
}
